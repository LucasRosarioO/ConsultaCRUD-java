    private void formatarCampo() {
        try {
            MaskFormatter dia = new MaskFormatter("####/##/##");
            dia.install(txtDia);
            dia.install(txtDiaExclusao);
            dia.install(txtDiaAlterar);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao formatar campo de texto.", "Error", JOptionPane.ERROR_MESSAGE);
        }

        try {
            MaskFormatter hora = new MaskFormatter("##:##:##");
            hora.install(txtHora);
            hora.install(txtHoraExclusao);
            hora.install(txtHoraAlterar);
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(null, "Erro ao formatar campo de texto.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

