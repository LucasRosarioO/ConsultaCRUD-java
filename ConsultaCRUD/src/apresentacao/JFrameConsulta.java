    private void jButtonInserirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInserirActionPerformed
        try {
            //Registra JDBC driver
            Class.forName("com.mysql.jdbc.Driver");

            //Abrindo a conexão: ATENÇÃO OS DOIS PARÂMETROS VAZIOS(" ") SÃO USUÁRIO E SENHA, RESPECTIVAMENTE.
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/clinica", "root", "root");

            //Executa a query de inserção
            java.sql.Statement st = conn.createStatement();
            st.executeUpdate("INSERT INTO consultas (codm, codp, data, hora) VALUES ("
                    + this.txtCodigoMedico.getText() + ",'"
                    + this.txtCodigoPaciente.getText() + "','"
                    + this.txtDia.getText() + "','"
                    + this.txtHora.getText() + "')");

            JOptionPane.showMessageDialog(rootPane, "Consulta Inserida");

        } catch (SQLException | ClassNotFoundException e) {
            JOptionPane.showMessageDialog(rootPane, e);
        }//Fim try

    }//GEN-LAST:event_jButtonInserirActionPerformed

